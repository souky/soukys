package com.jy.moudles.answer.entity;

public class Answer {

	private String id;
	private int sort;
	private String ablity;
	private String answer;
	private int score;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getSort() {
		return sort;
	}
	public void setSort(int sort) {
		this.sort = sort;
	}
	public String getAblity() {
		return ablity;
	}
	public void setAblity(String ablity) {
		this.ablity = ablity;
	}
	public String getAnswer() {
		return answer;
	}
	public void setAnswer(String answer) {
		this.answer = answer;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	
	
}
